export class Todo{
    constructor(id,
     name,
     status,
     createdAt,
     userName,
     lastUpdatedAt,
     completedAt){
        this.id = id
        this.name = name
        this.status = status
        this.createdAt = createdAt
        this.userName = userName
        this.lastUpdatedAt = lastUpdatedAt
        this.completedAt = completedAt
     }

}